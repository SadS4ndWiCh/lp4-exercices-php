<?php
    function sum(int $num1, int $num2) {
        return $num1 + $num2;
    };
    function sub(int $num1, int $num2) {
        return $num1 - $num2;
    };
    function mult(int $num1, int $num2) {
        return $num1 * $num2;
    };
    function div(int $num1, int $num2) {
        return $num1 / $num2;
    };